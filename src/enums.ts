/**
 * Enum que representa los niveles de prioridad de una tarea.
 */
export enum prioridad {
    /**
     * Prioridad baja.
     */
    baja = 0,
    /**
     * Prioridad media.
     */
    media = 1,
    /**
     * Prioridad alta.
     */
    alta = 2
}

